#ifndef PALINDROME_H
#define PALINDROME_H

#include <stdbool.h>

/// The function returns true if the string (null-terminated) is a palindrome,
/// false otherwise.
///
/// An empty string is considered a palindrome.
///
/// Case does not matter, and non-alphanumeric characters are ignored.
///
bool is_palindrome(const char * string);


#endif

